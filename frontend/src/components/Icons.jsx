export const getCategoryIcon = (catKey, transportPref) => {
  if (catKey === 'transportation') {
    if (transportPref === 'Train') return Icons.Train;
    if (transportPref === 'Bus') return Icons.Bus;
    if (transportPref === 'Cab') return Icons.LocalTransport;
    return Icons.Plane;
  }
  const map = {
    hotel: Icons.Hotel, food: Icons.Food, localTransport: Icons.LocalTransport,
    activities: Icons.Activities, shopping: Icons.Shopping,
    miscellaneous: Icons.Misc, emergencyFund: Icons.Emergency
  };
  return map[catKey] || Icons.Misc;
};
